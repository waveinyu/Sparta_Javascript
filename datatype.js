// 데이터 타입
// 프로그래밍이란 수많은 데이터를 입력, 처리, 출력하는 과정을 컴퓨터가 알아 듣는 언어로 서술하는 것
// 세상의 무수한 데이터들 사이 중에서, 컴퓨터가 보다 빠르고 효율적으로 처리하기 위해서 자바스크립트라는 프로그래밍 언어가 구분해놓은 것!
// 분류를 해놓으면 효율성이 올라가지 않니 ㅎㅎ

// 1. Number (숫자)
console.log(10);

let age = 31;
console.log(age);

// 2. String (문자열)
// 작은 따옴표나 큰 따옴표로 묶어서 표현
let name = "yuyu";
console.log(name);

// 3. Boolean (논리연산 : 참/거짓)
// 자바스크립트 - true/false / 파이썬 - True/False
let isMan = false; // False라는 boolean 데이터를 isMan이란 변수에 할당한 것
let isWoman = true;
console.log(isWoman);
console.log(isMan);

// 4. null과 undefined
// null : 값이 텅텅 비어 있는 것
// undefined : 변수를 선언만 한 채 값을 할당하지 않은 것
let nickname = null;
console.log(nickname); // null

let birthday;
console.log(birthday); // undefined
